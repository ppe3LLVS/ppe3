                         <h3 class="mt0">Les avantages</h3>
                        <p class="lead">La pratique de la balade présente de nombreux avantages :

La convivialité est au rendez-vous et permet de passer un bon moment à contempler dame nature sur le dos de son cheval.
On évolue dans un cadre idyllique qui apporte une grande bouffée d'air frais et vivifiant à tous les points de vue.
La balade à cheval est également une manière écologique de découvrir la nature qui nous entoure.
Enfin, on apprend à contrôler sa monture en extérieur et à gérer ses angoisses surtout quand on sort seul avec son cheval. </p>
                        <p class="lead">Pour les chevaux qui font autre chose que de la balade comme du manège, de la carrière ou des concours, cela leur permet de décompresser. C'est excellent pour leur mental et leur physique ! Au choix, on peut faire :

des balades tranquilles au pas pour les détendre ;
des trottings pour les rendre plus endurants sur le plan cardiaque ;
des galops, un peu de défoulement, qui ne font pas de mal et plaisent au cavalier comme au cheval.</p>
                        <p class="lead">Après plusieurs séances d'équitation, vous serez capable de guider votre cheval en toute autonomie. A vous de jouer !</p>
                        
