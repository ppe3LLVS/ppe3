                        <h3 class="mt0">Informations</h3>
                        <p class="lead">En groupe, une règle d'or est de conserver la place qui nous est attribuée avant de partir :

On se suit à la queue leu leu.
Le moniteur qui est responsable et dirige la balade se situe devant et les débutants sont placés au plus proche de lui pour pouvoir leur prodiguer des conseils.
Les cavaliers confirmés sont placés au milieu ou en dernier.
On doit conserver environ 1,50 mètre entre chaque cheval par mesure de sécurité. L'objectif est que :

Le cheval de devant ne tape pas celui de derrière.
Les distances soient conservées quand on change d'allure.
Une plus grande distance encourage le cheval qui traîne à rattraper son compagnon beaucoup plus rapidement. 
        </p>
                        <p class="lead"> C'est le moniteur qui décide des changements d'allure. Voici quelques règles à respecter :

On ne doit, en aucun cas, se doubler surtout quand on passe du trot au galop.
Un cheval qui passe devant un autre prend de la vitesse pour le doubler et peut taper un autre cheval, qui lui peut ruer, en embarquant toute la cavalerie. On court au désastre, surtout si les cavaliers ne sont pas tous de véritables confirmés dans le domaine.
Il est donc impératif de bien écouter les conseils du moniteur lors des changements d'allure.</p>
                      
