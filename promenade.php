                        <h3 class="mt0">Après le cours</h3>
                        <p class="lead">Quand la séance est terminée, votre cheval sera peut-être remonté par un autre cavalier. Si ce n'est pas le cas, cela vous laissera l’occasion de le rentrer au box et de le chouchouter.
N’hésitez pas à prendre du temps pour découvrir la vie du centre équestre. Vous pourrez ainsi regarder d’autres leçons et discuter ou vous amuser avec d’autres passionnés d'équitation.</p>
                        <p class="lead">Quand vous aurez bien progressé, vous pourrez voir avec votre moniteur pour passer les examens fédéraux appelés « galops ». Ils évaluent l’aisance du cavalier à cheval et sa position aux différentes allures. Les galops valident aussi les connaissances des cavaliers grâce à une épreuve théorique. Les galops vont de 1 à 9 mais en général, mais les cavaliers s'arrêtent souvent au galop 7 qui permet d'accéder aux compétitions amateurs ou professionnelles.</p>
                        
